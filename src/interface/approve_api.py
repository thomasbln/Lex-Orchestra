def _trigger_langgraph_scan(project_name: str, repo_url: str, dry_run: bool, run_id: str | None = None) -> None:
    """Run LangGraph compliance pipeline in background (dashboard-triggered).

    Telegram notification/delivery removed 2026-07-12 (retired chat-UI layer);
    scan progress is served by the /scan status page (ADR-068), documents by
    the /docs page. A CI/CD mode would re-add notification as a webhook
    abstraction here — not Telegram.
    """
    run_id = run_id or str(uuid.uuid4())

    initial_input = {
        "project_name":      project_name,
        "repo_url":          repo_url,
        "live_url":          None,
        "scan_depth":        "quick",
        "dry_run":           dry_run,
        "scout_result":      None,
        "graph_result":      None,
        "reasoning_result":  None,
        "generated_docs":    [],
        "notification_sent": False,
        "run_id":            run_id,
        "errors":            [],
    }
    try:
        thread_resp = http_requests.post(
            f"{LANGGRAPH_SERVER_URL}/threads",
            json={"thread_id": run_id},
            timeout=10,
        )
        thread_resp.raise_for_status()
        run_resp = http_requests.post(
            f"{LANGGRAPH_SERVER_URL}/threads/{run_id}/runs/wait",
            json={
                "assistant_id": "lex_workflow",
                "input": initial_input,
                "config": {"configurable": {"thread_id": run_id}},
            },
            timeout=SCAN_WAIT_TIMEOUT,
        )
        run_resp.raise_for_status()
        logger.info("scan completed: project=%s run_id=%s", project_name, run_id)
    except Exception as e:
        logger.error("scan failed: project=%s error=%s", project_name, e)
        # ADR-129 PR 7 (audit K6): on a wait-timeout the pipeline may still finish and
        # PATCH 'complete' later — only mark failed while the row is still 'running',
        # otherwise this would flicker failed→complete and lose the run.
        if _scan_results_status(run_id) in ("running", None):
            _scan_results_mark_failed(run_id, _classify_scan_error(e))
        else:
            logger.info("scan wait errored but run %s already left 'running' — not marking failed", run_id)


@app.post("/scan")
def trigger_scan(req: ScanRequest, background_tasks: BackgroundTasks) -> dict:
    """
    Trigger a compliance scan via LangGraph Server.
    Resolves repo_url from project_repos (is_primary=true), then runs pipeline in background.
    """
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")
    try:
        with psycopg2.connect(DB_URL) as conn:
            with conn.cursor() as cur:
                cur.execute("""
                    SELECT repo_url FROM project_repos
                    WHERE project_name = %s AND is_primary = TRUE AND is_active = TRUE
                    LIMIT 1
                """, (req.project_name,))
                row = cur.fetchone()
                if not row:
                    cur.execute("""
                        SELECT repo_url FROM project_tokens
                        WHERE project_name = %s
                    """, (req.project_name,))
                    row = cur.fetchone()
        if not row or not row[0]:
            raise HTTPException(status_code=404, detail=f"No repo_url found for {req.project_name}")
        repo_url = row[0]
    except HTTPException:
        raise
    except Exception as e:
        logger.error("scan lookup failed: %s", e)
        raise HTTPException(status_code=500, detail="internal error")

    # First-start gate (Launch-Gate "Erstkontakt"): a real scan against a
    # half-prepared system fails cryptically — refuse honestly instead.
    # dry_run stays ungated (infra-check uses it, needs no graph/LLM).
    if not req.dry_run:
        readiness = _system_readiness()
        if not readiness["ready"]:
            failing = ", ".join(k for k, v in readiness["checks"].items() if not v["ok"])
            raise HTTPException(status_code=503, detail=f"system preparing: {failing}")

    run_id = str(uuid.uuid4())

    # ADR-068: insert scan_results row BEFORE background task so status-polling
    # works from the very first request.
    try:
        _scan_results_insert_running(
            run_id=run_id,
            project_name=req.project_name,
            repo_url=repo_url,
            dry_run=req.dry_run,
            triggered_by=req.triggered_by,
        )
    except Exception as e:
        logger.error("scan_results insert failed: run_id=%s error=%s", run_id, e)
        raise HTTPException(status_code=500, detail="scan_results insert failed")

    background_tasks.add_task(_trigger_langgraph_scan, req.project_name, repo_url, req.dry_run, run_id)
    logger.info("scan queued: project=%s repo=%s dry_run=%s run_id=%s triggered_by=%s",
                req.project_name, repo_url, req.dry_run, run_id, req.triggered_by)
    return {
        "ok": True,
        "project_name": req.project_name,
        "repo_url": repo_url,
        "run_id": run_id,
        "scan_run_id": run_id,  # ADR-068 alias — same UUID, new field name for frontend clarity
    }


