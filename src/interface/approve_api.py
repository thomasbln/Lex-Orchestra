def _reaper_loop() -> None:
    """ADR-129 PR 4 (audit K3): the startup-only reaper left stuck 'running' rows
    until the next API restart — run it periodically instead."""
    while True:
        time.sleep(REAPER_INTERVAL_SECONDS)
        _reap_stale_scan_runs()


