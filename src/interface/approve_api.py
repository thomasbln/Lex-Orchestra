def _get_project_id_by_name(project_name: str, cur=None) -> str:
    """ADR-080: resolve project_name → project_config.id UUID.

    Inserts a stub project_config row if none exists yet — mirrors the
    ON CONFLICT DO NOTHING pattern callers were using before. Returns the
    UUID as a string (FastAPI JSON + psycopg2 params both accept str).

    Pass an open cursor to reuse the transaction; omit to open a fresh
    connection for one-shot lookups.
    """
    query_select = "SELECT id FROM project_config WHERE project_name = %s"
    query_insert = (
        "INSERT INTO project_config (project_name) VALUES (%s) "
        "ON CONFLICT (project_name) DO UPDATE SET project_name = EXCLUDED.project_name "
        "RETURNING id"
    )

    def _run(c):
        c.execute(query_select, (project_name,))
        row = c.fetchone()
        if row:
            return str(row[0])
        c.execute(query_insert, (project_name,))
        return str(c.fetchone()[0])

    if cur is not None:
        return _run(cur)
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")
    with psycopg2.connect(DB_URL) as conn, conn.cursor() as c:
        pid = _run(c)
        conn.commit()
        return pid


def _resolve_project_id(project_name: str, cur=None) -> str | None:
    """Resolve project_name → project_config.id UUID, read-only (ADR-125).

    Unlike _get_project_id_by_name, this NEVER inserts a stub row — the delete
    path must not resurrect the very project it is about to remove. Returns the
    UUID as a string, or None when no project_config row exists.
    """
    query = "SELECT id FROM project_config WHERE project_name = %s"

    def _run(c):
        c.execute(query, (project_name,))
        row = c.fetchone()
        return str(row[0]) if row else None

    if cur is not None:
        return _run(cur)
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")
    with psycopg2.connect(DB_URL) as conn, conn.cursor() as c:
        return _run(c)


# ── Vault helpers (ADR-083) ──────────────────────────────────────────────────

def _put_secret(cur, name: str, plaintext: str) -> str:
    """Create-or-rotate a vault secret by deterministic name. Returns UUID.

    vault.secrets has a UNIQUE constraint on `name`, so repeated calls
    with the same name must rotate (update) rather than re-create, or
    the second call fails with UniqueViolation. This is the only write
    helper — all vaulted credentials should go through here.
    """
    cur.execute("SELECT id FROM vault.secrets WHERE name = %s", (name,))
    row = cur.fetchone()
    if row:
        secret_id = str(row[0])
        cur.execute(
            "SELECT vault.update_secret(%s::uuid, %s, %s)",
            (secret_id, plaintext, name),
        )
        return secret_id
    cur.execute("SELECT vault.create_secret(%s, %s)", (plaintext, name))
    return str(cur.fetchone()[0])


def _save_integration_key(cur, project_id: str, integration: str, plaintext: str) -> str:
    """Store (or rotate) an integration credential in vault.secrets.

    Deterministic secret name `<integration>-<project_id>` → exactly one
    live secret per (project, integration). Re-posting with a new key
    rotates the ciphertext in place; the UUID reference stays stable.
    """
    return _put_secret(cur, f"{integration}-{project_id}", plaintext)


def _get_integration_key(cur, project_id: str, integration: str) -> str | None:
    """Decrypt the credential for (project, integration) or return None.

    RLS ensures only service_role can read vault.decrypted_secrets — the
    dashboard session (anon/authenticated JWT) cannot reach this path.
    """
    cur.execute(
        """
        SELECT ds.decrypted_secret
          FROM project_integrations pi
          JOIN vault.decrypted_secrets ds ON ds.id = pi.api_key_secret_id
         WHERE pi.project_id = %s
           AND pi.integration = %s
           AND pi.enabled = TRUE
        """,
        (project_id, integration),
    )
    row = cur.fetchone()
    return row[0] if row else None


def _get_integration_config(cur, project_id: str, integration: str) -> dict:
    """Return the JSONB config blob for (project, integration) or {}."""
    cur.execute(
        """
        SELECT config
          FROM project_integrations
         WHERE project_id = %s AND integration = %s
        """,
        (project_id, integration),
    )
    row = cur.fetchone()
    return row[0] if row and row[0] else {}


def _resolve_draft(filename: str) -> Path | None:
    LEGAL_DRAFTS_DIR.mkdir(parents=True, exist_ok=True)
    for candidate in [
        LEGAL_DRAFTS_DIR / filename,
        LEGAL_DRAFTS_DIR / f"{filename}.md",
    ]:
        if candidate.exists():
            return candidate
    stem = Path(filename).stem
    for f in LEGAL_DRAFTS_DIR.iterdir():
        if f.stem == stem and f.suffix == ".md":
            return f
    return None


# Committing/pushing approved drafts is a maintainer workflow — default off
# (same gate pattern as the former TELEGRAM_ENABLED). GIT_PUSH_REMOTE may
# embed credentials; there is deliberately no baked-in default remote.
GIT_PUSH_ENABLED = os.getenv("GIT_PUSH_ENABLED", "false").strip().lower() in ("1", "true", "yes")
GIT_PUSH_REMOTE = os.getenv("GIT_PUSH_REMOTE", "").strip()


def _git_env() -> dict:
    """safe.directory + committer identity via env-based git config (no HOME required)."""
    return {
        **os.environ,
        "GIT_CONFIG_COUNT": "3",
        "GIT_CONFIG_KEY_0": "safe.directory",
        "GIT_CONFIG_VALUE_0": str(REPO_ROOT),
        "GIT_CONFIG_KEY_1": "user.name",
        "GIT_CONFIG_VALUE_1": os.getenv("GIT_PUSH_USER", "Lex-Orchestra"),
        "GIT_CONFIG_KEY_2": "user.email",
        "GIT_CONFIG_VALUE_2": os.getenv("GIT_PUSH_EMAIL", "lex-orchestra@localhost"),
    }


def _git_commit_and_push(draft_path: Path, marker: Path) -> str:
    """Stage draft + marker, commit, push. Returns a status line."""
    if not GIT_PUSH_ENABLED:
        return "git push disabled (set GIT_PUSH_ENABLED=true to commit+push approvals)"
    try:
        rel_draft = str(draft_path.relative_to(REPO_ROOT))
        rel_marker = str(marker.relative_to(REPO_ROOT))

        env = _git_env()
        # Optional explicit push remote (may embed credentials) — no baked-in default.
        if GIT_PUSH_REMOTE:
            subprocess.run(
                ["git", "remote", "set-url", "origin", GIT_PUSH_REMOTE],
                cwd=REPO_ROOT, check=True, capture_output=True, env=env,
            )

        subprocess.run(
            ["git", "add", rel_draft, rel_marker],
            cwd=REPO_ROOT, check=True, capture_output=True, env=env,
        )
        result = subprocess.run(
            ["git", "commit", "-m", f"legal: approve {draft_path.name}"],
            cwd=REPO_ROOT, check=True, capture_output=True, text=True, env=env,
        )
        commit_line = result.stdout.strip().split("\n")[0]
        subprocess.run(
            ["git", "pull", "--rebase", "--autostash"],
            cwd=REPO_ROOT, check=True, capture_output=True, env=env,
        )
        subprocess.run(
            ["git", "push"],
            cwd=REPO_ROOT, check=True, capture_output=True, env=env,
        )
        return f"committed + pushed ({commit_line})"
    except subprocess.CalledProcessError as e:
        stderr = (e.stderr or b"")
        stdout = (e.stdout or b"")
        if isinstance(stderr, bytes):
            stderr = stderr.decode(errors="replace")
        if isinstance(stdout, bytes):
            stdout = stdout.decode(errors="replace")
        msg = (stderr + stdout).strip()
        logger.warning("Git failed (cmd=%s): %s", e.cmd, msg)
        return f"git failed: {msg[:300]}"


def _git_commit_and_push_all(markers: list[tuple[Path, Path]]) -> str:
    """Stage all drafts + markers, single commit, push. Returns a status line."""
    if not GIT_PUSH_ENABLED:
        return "git push disabled (set GIT_PUSH_ENABLED=true to commit+push approvals)"
    try:
        env = _git_env()
        # Optional explicit push remote (may embed credentials) — no baked-in default.
        if GIT_PUSH_REMOTE:
            subprocess.run(
                ["git", "remote", "set-url", "origin", GIT_PUSH_REMOTE],
                cwd=REPO_ROOT, check=True, capture_output=True, env=env,
            )

        files_to_stage = []
        for draft_path, marker in markers:
            files_to_stage.append(str(draft_path.relative_to(REPO_ROOT)))
            files_to_stage.append(str(marker.relative_to(REPO_ROOT)))

        subprocess.run(
            ["git", "add", *files_to_stage],
            cwd=REPO_ROOT, check=True, capture_output=True, env=env,
        )
        names = ", ".join(Path(f).stem for f, _ in markers)
        result = subprocess.run(
            ["git", "commit", "-m", f"legal: approve {len(markers)} drafts ({names})"],
            cwd=REPO_ROOT, check=True, capture_output=True, text=True, env=env,
        )
        commit_line = result.stdout.strip().split("\n")[0]
        subprocess.run(
            ["git", "pull", "--rebase", "--autostash"],
            cwd=REPO_ROOT, check=True, capture_output=True, env=env,
        )
        subprocess.run(
            ["git", "push"],
            cwd=REPO_ROOT, check=True, capture_output=True, env=env,
        )
        return f"committed + pushed ({commit_line})"
    except subprocess.CalledProcessError as e:
        stderr = (e.stderr or b"")
        stdout = (e.stdout or b"")
        if isinstance(stderr, bytes):
            stderr = stderr.decode(errors="replace")
        if isinstance(stdout, bytes):
            stdout = stdout.decode(errors="replace")
        msg = (stderr + stdout).strip()
        logger.warning("Git failed (cmd=%s): %s", e.cmd, msg)
        return f"git failed: {msg[:300]}"


