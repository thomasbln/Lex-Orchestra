# ── Repo check proxy (dashboard cannot reach api.github.com) ────────────────

@app.get("/utils/check-repo")
def check_repo(
    url: str = Query(..., description="GitHub repo URL"),
    token: str = Query("", description="GitHub personal access token"),
) -> dict:
    """Proxy GitHub API reachability check for the dashboard."""
    match = re.search(r"github\.com/([^/]+)/([^/\s]+?)(?:\.git)?/?$", url.strip())
    if not match:
        return {"status": "error", "message": "Invalid GitHub URL"}
    owner, repo = match.group(1), match.group(2)
    headers = {"User-Agent": "lex-orchestra/1.0"}
    if token:
        headers["Authorization"] = f"token {token}"
    try:
        resp = http_requests.get(
            f"https://api.github.com/repos/{owner}/{repo}",
            headers=headers,
            timeout=8,
        )
        if resp.status_code in (200, 301):
            return {"status": "accessible"}
        if resp.status_code == 401:
            return {"status": "invalid_token"}
        if resp.status_code in (403, 404):
            if token:
                return {"status": "invalid_token"}
            return {"status": "private"}
        return {"status": "error", "message": f"GitHub API returned {resp.status_code}"}
    except http_requests.exceptions.Timeout:
        logger.warning("check-repo timeout for %s — marking as accessible (URL valid)", url)
        # Pi may not have outbound internet — treat valid URL as accessible
        return {"status": "accessible"}
    except Exception as e:
        logger.warning("check-repo proxy failed: %s", e)
        # Network not reachable from container — treat valid URL as accessible
        return {"status": "accessible"}


