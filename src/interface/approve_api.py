# Tables whose project_id FK is ON DELETE SET NULL (ADR-080) — the hard-delete
# clears them explicitly to override the audit-orphan intent for this one path.
_HARD_DELETE_TABLES = ("generated_docs", "scan_signals", "scan_results")


def _collect_run_ids(cur, project_id: str) -> set[str]:
    """All run_ids tied to a project (ADR-125).

    Union across scan_results + generated_docs + scan_signals: generated_docs
    and scan_signals also cascade from scan_results via run_id, but unioning
    defensively surfaces a SET-NULL orphan's run_id (parent project_id nulled
    by a prior partial run) for filesystem cleanup.
    """
    run_ids: set[str] = set()
    for table in ("scan_results", "generated_docs", "scan_signals"):
        cur.execute(
            f"SELECT DISTINCT run_id FROM {table} "
            f"WHERE project_id = %s AND run_id IS NOT NULL",
            (project_id,),
        )
        run_ids.update(str(r[0]) for r in cur.fetchall())
    return run_ids


def _collect_secret_ids(cur, project_id: str) -> set[str]:
    """All vault.secrets UUIDs referenced by a project's rows (ADR-083)."""
    secret_ids: set[str] = set()
    queries = (
        "SELECT api_key_secret_id FROM project_integrations WHERE project_id = %s",
        "SELECT github_token_secret_id FROM project_tokens WHERE project_id = %s",
        "SELECT github_token_secret_id FROM project_repos WHERE project_id = %s",
    )
    for q in queries:
        cur.execute(q, (project_id,))
        secret_ids.update(str(r[0]) for r in cur.fetchall() if r[0])
    return secret_ids


def _safe_draft_files(run_ids: set[str]) -> list[Path]:
    """Resolved draft files carrying a project's run_id, contained under the drafts dir.

    Security (ADR-125): the run_id whitelist is the ONLY path input. Every
    candidate is realpath-resolved and must sit under the resolved drafts dir —
    a string-prefix check would let a symlink escape. project_name is never
    interpolated into a path.
    """
    base = LEGAL_DRAFTS_DIR.resolve()
    if not base.is_dir():
        return []
    short_ids = {rid[:8] for rid in run_ids if rid}
    if not short_ids:
        return []
    out: list[Path] = []
    for entry in base.iterdir():
        # Draft names are <doctype>_<run_id[:8]>.<ext> / <run_id[:8]>.<ext>.
        if not any(sid in entry.name for sid in short_ids):
            continue
        target = entry.resolve()
        try:
            target.relative_to(base)  # realpath containment, symlink-safe
        except ValueError:
            continue
        if target.is_file():
            out.append(target)
    return out


