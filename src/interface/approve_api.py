class RetentionPolicyIn(BaseModel):
    category:      str
    duration_days: int | None = None
    duration_raw:  str
    source:        Literal["code", "setup", "firecrawl"] = "setup"


class ProjectSetupPayload(BaseModel):
    on_prem:             bool = False
    hosting_provider:    str | None = None
    hosting_region:      str | None = None
    retention_policies:  list[RetentionPolicyIn] = Field(default_factory=list)
    created_by:          str | None = None


@app.post("/projects/{project_name}/setup")
def save_project_setup(project_name: str, payload: ProjectSetupPayload) -> dict:
    """Append-only revision + upsert of current pointer (ADR-076).

    Every save writes a new project_setup_revisions row and updates the
    project_setups current pointer. retention_policies with source='setup'
    are replaced wholesale — code/firecrawl sources are preserved.
    """
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")
    try:
        with psycopg2.connect(DB_URL) as conn:
            with conn.cursor() as cur:
                # ADR-080: resolve project_id for FK writes
                project_id = _get_project_id_by_name(project_name, cur)

                # Upsert current-pointer first so the FK from revisions resolves.
                # PK is project_id (see migration 014).
                cur.execute("""
                    INSERT INTO project_setups (
                        project_id, project_name, on_prem,
                        hosting_provider, hosting_region, updated_at
                    ) VALUES (%s, %s, %s, %s, %s, now())
                    ON CONFLICT (project_id) DO UPDATE SET
                        project_name       = EXCLUDED.project_name,
                        on_prem            = EXCLUDED.on_prem,
                        hosting_provider   = EXCLUDED.hosting_provider,
                        hosting_region     = EXCLUDED.hosting_region,
                        updated_at         = now()
                """, (project_id, project_name, payload.on_prem,
                      payload.hosting_provider, payload.hosting_region))

                # Append-only revision
                cur.execute("""
                    INSERT INTO project_setup_revisions (project_id, project_name, data, created_by)
                    VALUES (%s, %s, %s::jsonb, %s)
                    RETURNING id
                """, (project_id, project_name, payload.model_dump_json(), payload.created_by))
                revision_id = cur.fetchone()[0]

                # Point current_revision_id at the revision we just inserted
                cur.execute(
                    "UPDATE project_setups SET current_revision_id = %s WHERE project_id = %s",
                    (revision_id, project_id),
                )

                # Retention policies — replace all setup-sourced rows wholesale;
                # code/firecrawl entries (if any) are preserved.
                cur.execute(
                    "DELETE FROM retention_policies WHERE project_id = %s AND source = 'setup'",
                    (project_id,),
                )
                for rp in payload.retention_policies:
                    cur.execute("""
                        INSERT INTO retention_policies
                            (project_id, project_name, category, duration_days, duration_raw, source)
                        VALUES (%s, %s, %s, %s, %s, 'setup')
                        ON CONFLICT (project_id, category) DO UPDATE SET
                            project_name  = EXCLUDED.project_name,
                            duration_days = EXCLUDED.duration_days,
                            duration_raw  = EXCLUDED.duration_raw,
                            source        = 'setup',
                            updated_at    = now()
                    """, (project_id, project_name, rp.category, rp.duration_days, rp.duration_raw))

            conn.commit()
        logger.info("project_setup saved: project=%s revision=%s", project_name, revision_id)
        return {"project_name": project_name, "revision_id": str(revision_id)}
    except Exception as e:
        logger.error("project_setup save failed: project=%s error=%s", project_name, e)
        raise HTTPException(status_code=500, detail="internal error")


