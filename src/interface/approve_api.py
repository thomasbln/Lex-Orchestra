@app.delete("/projects/{project_name}")
def delete_project(project_name: str) -> dict:
    """Hard-delete a project and all of its data (ADR-125).

    Order: filesystem → vault + DB (last). project_config stays the
    source-of-truth that cleanup is pending, so a crash mid-run is self-healing
    on re-run (files already gone → no-op, DB transaction completes).
    Idempotent: 404 when the project is already fully gone.
    """
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")

    # 1–3: resolve + collect (read-only).
    with psycopg2.connect(DB_URL) as conn, conn.cursor() as cur:
        project_id = _resolve_project_id(project_name, cur)
        if project_id is None:
            raise HTTPException(status_code=404, detail="project not found")
        run_ids = _collect_run_ids(cur, project_id)
        secret_ids = _collect_secret_ids(cur, project_id)

    # 4: filesystem first — verwaiste Files ohne DB-Referenz vermeiden.
    files_deleted = 0
    for target in _safe_draft_files(run_ids):
        try:
            target.unlink()
            files_deleted += 1
        except OSError as e:
            logger.warning("draft delete skipped %s: %s", target.name, e)

    # 5 + 6: vault + DB last, one atomic transaction (vault.secrets is the same
    # Postgres DB — cf. disconnect_integration).
    deleted = {"scans": 0, "signals": 0, "docs": 0,
               "vault_secrets": len(secret_ids), "files": files_deleted}
    with psycopg2.connect(DB_URL) as conn, conn.cursor() as cur:
        for sid in secret_ids:
            cur.execute("DELETE FROM vault.secrets WHERE id = %s", (sid,))
        # Children before parent so rowcounts are exact (no run_id-cascade overlap).
        cur.execute("DELETE FROM generated_docs WHERE project_id = %s", (project_id,))
        deleted["docs"] = cur.rowcount
        cur.execute("DELETE FROM scan_signals WHERE project_id = %s", (project_id,))
        deleted["signals"] = cur.rowcount
        cur.execute("DELETE FROM scan_results WHERE project_id = %s", (project_id,))
        deleted["scans"] = cur.rowcount
        # CASCADE clears project_tokens/repos/setups/revisions/retention/integrations.
        cur.execute("DELETE FROM project_config WHERE id = %s", (project_id,))
        conn.commit()

    logger.info("project deleted: %s (%d files, %d secrets, %d scans)",
                project_name, files_deleted, len(secret_ids), deleted["scans"])
    return {"ok": True, "deleted": deleted}


