@app.post("/approve")
def approve_draft(req: ApproveRequest) -> dict:
    """
    Mark a legal draft as approved and commit + push the marker file.
    Returns {"ok": true, "draft": "...", "git": "..."}.
    """
    draft_path = _resolve_draft(req.filename)
    if draft_path is None:
        raise HTTPException(status_code=404, detail=f"Draft not found: {req.filename}")

    marker = draft_path.with_suffix(".approved")
    marker.write_text("Approved\n", encoding="utf-8")
    logger.info("Draft approved: %s", draft_path.name)

    git_status = _git_commit_and_push(draft_path, marker)

    return {
        "ok": True,
        "draft": draft_path.name,
        "marker": marker.name,
        "git": git_status,
    }


@app.post("/reject")
def reject_draft(req: RejectRequest) -> dict:
    """
    Mark a legal draft as rejected with an optional note.
    Returns {"ok": true, "draft": "...", "marker": "..."}.
    """
    draft_path = _resolve_draft(req.filename)
    if draft_path is None:
        raise HTTPException(status_code=404, detail=f"Draft not found: {req.filename}")

    marker = draft_path.with_suffix(".rejected")
    content = "Rejected\n"
    if req.note:
        content += f"Note: {req.note}\n"
    marker.write_text(content, encoding="utf-8")
    logger.info("Draft rejected: %s | note: %s", draft_path.name, req.note or "(none)")

    return {
        "ok": True,
        "draft": draft_path.name,
        "marker": marker.name,
    }


@app.post("/approve/all")
def approve_all_drafts() -> dict:
    """
    Approve all pending drafts in one operation with a single git commit + push.
    Returns {"ok": true, "approved": [...], "git": "..."}.
    """
    LEGAL_DRAFTS_DIR.mkdir(parents=True, exist_ok=True)
    pending = [
        f for f in sorted(LEGAL_DRAFTS_DIR.iterdir(), key=lambda x: x.stat().st_mtime)
        if f.suffix == ".md"
        and not f.with_suffix(".approved").exists()
        and not f.with_suffix(".rejected").exists()
    ]

    if not pending:
        return {"ok": True, "approved": [], "git": "nothing to approve"}

    markers = []
    approved_names = []
    for draft_path in pending:
        marker = draft_path.with_suffix(".approved")
        marker.write_text("Approved\n", encoding="utf-8")
        markers.append((draft_path, marker))
        approved_names.append(draft_path.stem)
        logger.info("Draft approved: %s", draft_path.name)

    # Single git commit + push for all drafts
    git_status = _git_commit_and_push_all(markers)

    return {"ok": True, "approved": approved_names, "git": git_status}


@app.get("/drafts")
def list_drafts() -> dict:
    """List all pending (unapproved, unrejected) drafts, newest first."""
    LEGAL_DRAFTS_DIR.mkdir(parents=True, exist_ok=True)
    drafts = []
    for f in sorted(LEGAL_DRAFTS_DIR.iterdir(), key=lambda x: x.stat().st_mtime, reverse=True):
        if f.suffix != ".md":
            continue
        if not f.with_suffix(".approved").exists() and not f.with_suffix(".rejected").exists():
            drafts.append(f.stem)
    return {"drafts": drafts}


