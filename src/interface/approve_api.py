@app.get("/graph/node-edges")
async def get_node_edges(node_type: str = Query(...), node_id: str = Query(...)) -> dict:
    """Returns all edges (outgoing + incoming) for a specific node."""
    if node_type not in _ALLOWED_NODE_TYPES:
        raise HTTPException(status_code=400, detail="Unknown node type")
    gc = _get_graph_client()

    outgoing = gc.run_query(f"""
        MATCH (n:{node_type})
        WHERE n.id = $nid OR n.name = $nid
        OPTIONAL MATCH (n)-[r]->(m)
        RETURN type(r) AS rel, labels(m)[0] AS target_type,
               coalesce(m.id, m.name, m.type, m.level, m.title_en, m.title_de, m.title) AS target_id,
               coalesce(m.title_en, m.title_de, m.name_de, m.type, m.name) AS target_label
    """, {"nid": node_id})

    incoming = gc.run_query(f"""
        MATCH (n:{node_type})
        WHERE n.id = $nid OR n.name = $nid
        OPTIONAL MATCH (m)-[r]->(n)
        RETURN type(r) AS rel, labels(m)[0] AS source_type,
               coalesce(m.id, m.name, m.type, m.level, m.title_en, m.title_de, m.title) AS source_id,
               coalesce(m.title_en, m.title_de, m.name_de, m.type, m.name) AS source_label
    """, {"nid": node_id})

    out = [
        {"direction": "out", "rel": r["rel"], "target_type": r["target_type"],
         "target_id": r["target_id"], "target_label": r["target_label"]}
        for r in outgoing if r["rel"]
    ]
    inc = [
        {"direction": "in", "rel": r["rel"], "source_type": r["source_type"],
         "source_id": r["source_id"], "source_label": r["source_label"]}
        for r in incoming if r["rel"]
    ]
    return {"edges": out + inc}


@app.get("/graph/ego")
async def get_ego_graph(
    node_type: str = Query(...),
    node_id: str = Query(...),
) -> dict:
    """Return ego-graph: selected node + all direct neighbors (1 hop)."""
    if node_type not in _ALLOWED_NODE_TYPES:
        raise HTTPException(status_code=400, detail="Unknown node type")

    gc = _get_graph_client()
    result = gc.run_query(f"""
        MATCH (center:{node_type})
        WHERE center.id = $nid OR center.name = $nid OR center.type = $nid OR center.short = $nid
        OPTIONAL MATCH (center)-[r]->(neighbor)
        OPTIONAL MATCH (other)-[r2]->(center)
        RETURN
            properties(center) AS center_props,
            labels(center)[0] AS center_type,
            collect(DISTINCT {{
                rel: type(r),
                direction: 'out',
                node_type: labels(neighbor)[0],
                node_props: properties(neighbor)
            }}) AS outgoing,
            collect(DISTINCT {{
                rel: type(r2),
                direction: 'in',
                node_type: labels(other)[0],
                node_props: properties(other)
            }}) AS incoming
        LIMIT 1
    """, {"nid": node_id})

    if not result:
        raise HTTPException(status_code=404, detail="Node not found")

    row = result[0]
    nodes: list[dict] = []
    edges: list[dict] = []

    center_id = str(
        row["center_props"].get("id")
        or row["center_props"].get("name")
        or row["center_props"].get("type")
        or row["center_props"].get("level")
        or node_id
    )
    nodes.append({
        "id": center_id,
        "type": row["center_type"],
        "props": row["center_props"],
        "is_center": True,
    })

    for item in row["outgoing"]:
        if not item.get("node_type") or not item.get("node_props"):
            continue
        n_id = str(
            item["node_props"].get("id")
            or item["node_props"].get("name")
            or item["node_props"].get("type")
            or ""
        )
        if not n_id:
            continue
        if not any(n["id"] == n_id for n in nodes):
            nodes.append({"id": n_id, "type": item["node_type"], "props": item["node_props"], "is_center": False})
        edges.append({"from": center_id, "to": n_id, "rel": item["rel"], "direction": "out"})

    for item in row["incoming"]:
        if not item.get("node_type") or not item.get("node_props"):
            continue
        n_id = str(
            item["node_props"].get("id")
            or item["node_props"].get("name")
            or item["node_props"].get("type")
            or ""
        )
        if not n_id:
            continue
        if not any(n["id"] == n_id for n in nodes):
            nodes.append({"id": n_id, "type": item["node_type"], "props": item["node_props"], "is_center": False})
        edges.append({"from": n_id, "to": center_id, "rel": item["rel"], "direction": "in"})

    return {"nodes": nodes, "edges": edges, "center_id": center_id}


