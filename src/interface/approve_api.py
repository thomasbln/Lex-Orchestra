def _load_integration_catalog() -> list[dict]:
    """Read Service nodes with category='integration' from the graph.

    config_schema is stored as a JSON string in Neo4j (node props are flat);
    parse it here so the dashboard gets a real object.
    """
    gc = _get_graph_client()
    rows = gc.run_query("""
        MATCH (svc:Service {category: 'integration'})
        RETURN svc.name                 AS name,
               svc.subcategory          AS subcategory,
               svc.capabilities         AS capabilities,
               svc.required_credentials AS required_credentials,
               svc.config_schema        AS config_schema,
               svc.pricing_tier         AS pricing_tier,
               svc.documentation_url    AS documentation_url,
               svc.region               AS region,
               svc.requires_scc         AS requires_scc
        ORDER BY svc.name
    """)
    catalog = []
    for r in rows:
        schema_raw = r.get("config_schema")
        try:
            schema = json.loads(schema_raw) if schema_raw else {}
        except (TypeError, ValueError):
            schema = {}
        catalog.append({
            "name":                 r["name"],
            "subcategory":          r.get("subcategory"),
            "capabilities":         r.get("capabilities") or [],
            "required_credentials": r.get("required_credentials") or [],
            "config_schema":        schema,
            "pricing_tier":         r.get("pricing_tier"),
            "documentation_url":    r.get("documentation_url"),
            "region":               r.get("region"),
            "requires_scc":         bool(r.get("requires_scc")),
        })
    return catalog


