def _scan_runs_meta(cur, short_ids: list[str]) -> list[dict]:
    """Scan-select metadata: one entry per known run (8-char key), newest first.

    Shared by /docs and /logs (row 58 / part 3 — no duplicate query). Sorted by
    COALESCE(started_at, completed_at) so direct-path runs with started_at=NULL
    rank honestly. Runs missing from the result have no scan_results row
    (project deleted) — the UI owns that labeling.
    """
    if not short_ids:
        return []
    cur.execute(
        """
        SELECT left(run_id::text, 8), run_id::text, project_name,
               COALESCE(started_at, completed_at), status
        FROM scan_results
        WHERE left(run_id::text, 8) = ANY(%s)
        ORDER BY COALESCE(started_at, completed_at) DESC NULLS LAST
        """,
        (short_ids,),
    )
    seen: set[str] = set()
    out: list[dict] = []
    for short, full, pname, ts, status in cur.fetchall():
        if short in seen:
            continue  # 8-char collision: newest wins (ORDER BY above)
        seen.add(short)
        out.append({"run_id": short, "run_id_full": full, "project_name": pname,
                    "scan_time": ts.isoformat() if ts else None, "status": status})
    return out


@app.get("/docs")
def list_docs(
    run_id: str | None = Query(default=None),
    limit: int = Query(default=200, le=500),
) -> dict:
    """List generated compliance docs from legal/drafts/, newest first."""
    if not LEGAL_DRAFTS_DIR.exists():
        return {"docs": [], "total": 0}
    docs = []
    for f in sorted(LEGAL_DRAFTS_DIR.glob("*.md"), key=lambda x: x.stat().st_mtime, reverse=True):
        stem = f.stem
        parts = stem.split("_")
        run = parts[-1] if len(parts) > 1 else "unknown"
        doc_type = "_".join(parts[:-1]) if len(parts) > 1 else stem
        if run_id and run != run_id[:8]:
            continue
        stat = f.stat()
        pdf = f.with_suffix(".pdf")
        # ADR-111: the provenance logbook is written with the uppercase doc_type
        # key (AVV/VVT/SCC), while the .md/.pdf use the lowercase stem — so the
        # companion is "<DOCTYPE>_<run>.logbook.json", not f.with_suffix(...).
        # Only the three PSP-relevant doc types have a logbook today.
        logbook = LEGAL_DRAFTS_DIR / f"{doc_type.upper()}_{run}.logbook.json"
        # ADR-112: the retrieval trace is per-RUN (one file for all docs of a run),
        # not per-doc — `<run8>.retrieval-trace.json`. Every doc of a run carries
        # the same value; the UI surfaces it once at run level.
        trace = LEGAL_DRAFTS_DIR / f"{run}.retrieval-trace.json"
        docs.append({
            "filename": f.name,
            "doc_type": doc_type,
            "run_id": run,
            "created_at": stat.st_mtime,
            "size_bytes": stat.st_size,
            "pdf_filename": pdf.name if pdf.exists() else None,
            "logbook_filename": logbook.name if logbook.exists() else None,
            "retrieval_trace_filename": trace.name if trace.exists() else None,
        })
    docs = docs[:limit]

    # ADR-127 P4.5/P5d: two DB-derived additions (best-effort, single cursor).
    #  (1) run_id_full per doc — the /docs `run_id` is the 8-char filename prefix
    #      (display label); the measures editor needs the full UUID. generated_docs
    #      maps file basename → run_id (verified 1:1 — no file_path has >1 run_id).
    #  (2) editable_run_ids = FULL UUIDs of the latest run WITH an owner_measures
    #      snapshot per project (authoritative = has snapshot + newest started_at;
    #      NOT status='complete', RoT#1).
    # One id-world for logic (full UUID); 8-char `run_id` stays as the tab label.
    # DB down → run_id_full=None + editable_run_ids=[] → UI conservatively disables
    # every edit button. Additive fields; existing consumers ignore them.
    editable_run_ids: list[str] = []
    runs_meta: list[dict] = []
    if DB_URL:
        try:
            with psycopg2.connect(DB_URL) as conn, conn.cursor() as cur:
                cur.execute("SELECT file_path, run_id FROM generated_docs")
                name_to_uuid = {Path(fp).name: str(rid) for fp, rid in cur.fetchall()}
                for d in docs:
                    d["run_id_full"] = name_to_uuid.get(d["filename"])
                # "latest" must not depend on started_at alone: direct-path runs
                # (pipeline _write_scan_result without the POST /scan lifecycle)
                # historically had started_at=NULL and were demoted by NULLS LAST
                # (row 58, 2026-07-19). completed_at is the honest fallback.
                cur.execute(
                    """
                    SELECT DISTINCT ON (om.project_id) om.run_id
                    FROM owner_measures om
                    JOIN scan_results sr ON sr.run_id = om.run_id
                    ORDER BY om.project_id,
                             COALESCE(sr.started_at, sr.completed_at) DESC NULLS LAST
                    """
                )
                editable_run_ids = [str(r[0]) for r in cur.fetchall()]
                # Scan-select metadata (additive): file-runs WITHOUT a
                # scan_results row (project deleted) are deliberately absent —
                # the UI labels them "(Projekt gelöscht)". An empty/missing
                # `runs` (DB down) means "no metadata", NOT "all orphans".
                run_short_ids = sorted({d["run_id"] for d in docs
                                        if d.get("run_id") and d["run_id"] != "unknown"})
                runs_meta = _scan_runs_meta(cur, run_short_ids)
        except Exception as e:
            logger.warning("docs DB enrichment failed (non-fatal): %s", e)
            editable_run_ids = []
            runs_meta = []
            for d in docs:
                d.setdefault("run_id_full", None)
    else:
        for d in docs:
            d["run_id_full"] = None

    return {"docs": docs, "total": len(docs), "editable_run_ids": editable_run_ids,
            "runs": runs_meta}


@app.get("/docs/file/{filename}")
def get_doc_content(filename: str) -> dict:
    """Return markdown content of a specific doc from legal/drafts/."""
    if ".." in filename or "/" in filename or not filename.endswith(".md"):
        raise HTTPException(status_code=400, detail="Invalid filename")
    doc_path = LEGAL_DRAFTS_DIR / filename
    if not doc_path.exists():
        raise HTTPException(status_code=404, detail="Doc not found")
    return {"filename": filename, "content": doc_path.read_text(encoding="utf-8")}


