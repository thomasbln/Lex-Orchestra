@app.get("/scan/{run_id}/measures")
def get_run_measures(run_id: str) -> dict:
    """ADR-127 P5a — owner-measure overlay for a run's editor.

    Per control_id: {de:{...}, en:{...}|null, deleted:bool}. Both langs returned raw
    (not pre-merged) — the editor decides what to show. Source = owner_measures
    snapshot + owner edits (P4.2/P4.3) + deleted_controls (lang-agnostic).
    404 if the run has no snapshot (unknown run / pre-P4.2).
    """
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")
    try:
        with psycopg2.connect(DB_URL) as conn:
            with conn.cursor() as cur:
                cur.execute("SELECT project_name FROM scan_results WHERE run_id = %s", (run_id,))
                row = cur.fetchone()
                if not row:
                    raise HTTPException(status_code=404, detail=f"Unknown run_id: {run_id}")
                project_name = row[0]
                cur.execute(
                    "SELECT control_id, lang, framework, title, default_text, text, edited_flag, source "
                    "FROM owner_measures WHERE run_id = %s ORDER BY control_id, lang",
                    (run_id,),
                )
                rows = cur.fetchall()
                if not rows:
                    raise HTTPException(status_code=404, detail=f"No measures snapshot for run {run_id}")
                cur.execute("SELECT control_id FROM deleted_controls WHERE run_id = %s", (run_id,))
                deleted = {r[0] for r in cur.fetchall()}
        measures: dict = {}
        for control_id, lang, framework, title, default_text, text, edited_flag, source in rows:
            entry = measures.setdefault(
                control_id, {"de": None, "en": None, "deleted": control_id in deleted}
            )
            entry[lang] = {
                "framework": framework, "title": title, "default_text": default_text,
                "text": text, "edited_flag": edited_flag, "source": source,
            }
        return {"ok": True, "run_id": run_id, "project_name": project_name, "measures": measures}
    except HTTPException:
        raise
    except Exception as e:
        logger.error("get_run_measures failed for %s: %s", run_id, e)
        raise HTTPException(status_code=500, detail="internal error")


@app.post("/scan/{run_id}/measures")
def edit_run_measure(run_id: str, req: MeasureActionRequest) -> dict:
    """ADR-127 P5a — apply one owner action to a run's measures.

    action: edit (set text + edited_flag=true, source='owner') | reset (clear back to
    default) | deactivate (add to deleted_controls) | reactivate (remove). Run-scoped;
    project_id resolved from the run's project.

    edit upserts language-pure (ADR-129 B-1, rule 3): if the (control_id, lang) row
    does not exist but a sibling-lang row does, the edit CREATES the active-lang row
    (title from request or sibling; framework from sibling; default_text stays NULL —
    there is no snapshot for that lang, and we never copy the other language's text).
    `updated=0` is returned honestly only for a control_id unknown in ANY lang
    (no blind insert without snapshot fields). reset stays a plain UPDATE.
    """
    # ADR-129 PR 1: input guards before any DB work — unknown action/lang are already
    # rejected by the Literal fields (422); these cover the per-action required fields.
    if req.action != "add" and not req.control_id:
        raise HTTPException(status_code=422, detail=f"control_id required for action '{req.action}'")
    if req.action == "add" and (not (req.title or "").strip() or not (req.text or "").strip()):
        raise HTTPException(status_code=422, detail="title and text required for action 'add'")
    # ADR-129 PR N5 (re-audit B-5): an edit that carries neither text nor title
    # would only flip edited_flag/source — a no-op with owner provenance. Reject.
    if req.action == "edit" and req.text is None and req.title is None:
        raise HTTPException(status_code=422, detail="text or title required for action 'edit'")
    if not DB_URL:
        raise HTTPException(status_code=500, detail="DATABASE_URL not set")
    try:
        with psycopg2.connect(DB_URL) as conn:
            with conn.cursor() as cur:
                cur.execute("SELECT project_name FROM scan_results WHERE run_id = %s", (run_id,))
                row = cur.fetchone()
                if not row:
                    raise HTTPException(status_code=404, detail=f"Unknown run_id: {run_id}")
                project_name = row[0]
                project_id = _get_project_id_by_name(project_name, cur)

                if req.action == "edit":
                    # PR5e-5: title rides along for custom measures (owner-editable).
                    # ADR-129 PR N5 (re-audit B-5/B-6): NULL *and* empty string can
                    # never clobber — a title-only edit keeps the body (the old
                    # `text = %s` silently NULLed it with edited_flag=true), and a
                    # cleared title keeps the old one (an empty title made the row
                    # anonymous in the doc and invisible to the editor). Clearing a
                    # text back to the default is `reset`, deleting a custom is
                    # `delete` — edit never empties.
                    cur.execute(
                        "UPDATE owner_measures SET "
                        "text = COALESCE(NULLIF(%s, ''), text), "
                        "title = COALESCE(NULLIF(%s, ''), title), "
                        "edited_flag = true, source = 'owner', updated_at = now() "
                        "WHERE project_id = %s AND run_id = %s AND control_id = %s AND lang = %s",
                        (req.text, req.title, project_id, run_id, req.control_id, req.lang),
                    )
                    if cur.rowcount == 0:
                        # ADR-129 B-1 rule 3: no row for this lang yet — the owner is
                        # writing the first text of THIS language. Create it from the
                        # sibling-lang row (identity/framework only; default_text stays
                        # NULL, the other language's text is NEVER copied). Matches 0
                        # rows when the control_id is unknown in any lang → updated=0.
                        cur.execute(
                            "INSERT INTO owner_measures (project_id, project_name, control_id, "
                            "run_id, lang, text, title, framework, edited_flag, source) "
                            "SELECT project_id, project_name, control_id, run_id, %s, "
                            "NULLIF(%s, ''), COALESCE(NULLIF(%s, ''), title), framework, "
                            "true, 'owner' "
                            "FROM owner_measures "
                            "WHERE project_id = %s AND run_id = %s AND control_id = %s "
                            "LIMIT 1",
                            (req.lang, req.text, req.title,
                             project_id, run_id, req.control_id),
                        )
                elif req.action == "reset":
                    cur.execute(
                        "UPDATE owner_measures SET text = NULL, edited_flag = false, "
                        "source = 'lex-llm-draft', updated_at = now() "
                        "WHERE project_id = %s AND run_id = %s AND control_id = %s AND lang = %s",
                        (project_id, run_id, req.control_id, req.lang),
                    )
                elif req.action == "deactivate":
                    cur.execute(
                        "INSERT INTO deleted_controls (project_id, project_name, run_id, control_id) "
                        "VALUES (%s, %s, %s, %s) ON CONFLICT DO NOTHING",
                        (project_id, project_name, run_id, req.control_id),
                    )
                elif req.action == "reactivate":
                    cur.execute(
                        "DELETE FROM deleted_controls "
                        "WHERE project_id = %s AND run_id = %s AND control_id = %s",
                        (project_id, run_id, req.control_id),
                    )
                elif req.action == "add":
                    # ADR-127 PR5e: owner-authored custom measure — one lang row (MVP=de),
                    # server-generated id, TOM-only. default_text stays NULL (no graph default).
                    req_cid = f"custom-{uuid.uuid4().hex[:8]}"
                    cur.execute(
                        "INSERT INTO owner_measures (project_id, project_name, control_id, run_id, "
                        "lang, text, title, framework, edited_flag, source) "
                        "VALUES (%s, %s, %s, %s, %s, %s, %s, %s, true, 'owner')",
                        (project_id, project_name, req_cid, run_id, req.lang,
                         req.text, req.title, req.framework or "Custom"),
                    )
                else:  # delete — custom rows only (scan measures are deactivate-only)
                    if not (req.control_id or "").startswith("custom-"):
                        raise HTTPException(status_code=400, detail="Only custom measures can be deleted")
                    req_cid = req.control_id
                    cur.execute(
                        "DELETE FROM owner_measures "
                        "WHERE project_id = %s AND run_id = %s AND control_id = %s",
                        (project_id, run_id, req_cid),
                    )
                    # rowcount of the measure DELETE — the toggle cleanup below would
                    # report 0 for an already-active row (ADR-129 PR 1, audit NTH).
                    updated = cur.rowcount
                    # clean up any active-toggle (deleted_controls) entry — no orphan
                    cur.execute(
                        "DELETE FROM deleted_controls "
                        "WHERE project_id = %s AND run_id = %s AND control_id = %s",
                        (project_id, run_id, req_cid),
                    )
                if req.action != "delete":
                    updated = cur.rowcount
            conn.commit()
        return {
            "ok": True, "run_id": run_id, "action": req.action,
            "control_id": (req_cid if req.action in ("add", "delete") else req.control_id),
            "lang": req.lang, "updated": updated,
        }
    except HTTPException:
        raise
    except Exception as e:
        logger.error("edit_run_measure failed for %s: %s", run_id, e)
        raise HTTPException(status_code=500, detail="internal error")


@app.post("/scan/{run_id}/rerender")
def rerender_run(run_id: str) -> dict:
    """ADR-127 P4.6 — re-render an existing run's documents WITHOUT a re-scan.

    Reconstructs graph_result via a read-only graph re-query (Option A):
    get_compliance_requirements — the SAME query the pipeline uses — fed from the
    run's persisted `service_detected` signals (scan_signals) + the project's
    `ai_usecase_type` (project_config). risk_signals are the run's non-service
    scan_signals. Then runs the document architect with the EXISTING run_id, so
    files overwrite in place (tom_{run_id[:8]}.md etc., deterministic) — same tab,
    no duplicate. The owner_measures overlay (P4.3) applies automatically (owner
    edits win > config > default; deleted_controls skip).

    NO scout, NO file-scan, NO graph-enrichment node — only this read-only graph
    query + Supabase reads. `integration_mode` (ADR-110) is transient/not persisted
    → not reconstructed (accepted minor loss; payment-mode hint falls to default).
    """
    if not DB_URL:
        raise HTTPException(status_code=503, detail="Database not configured")
    try:
        with psycopg2.connect(DB_URL) as conn:
            with conn.cursor() as cur:
                cur.execute("SELECT project_name FROM scan_results WHERE run_id = %s", (run_id,))
                row = cur.fetchone()
                if not row:
                    raise HTTPException(status_code=404, detail=f"Unknown run_id: {run_id}")
                project_name = row[0]

                cur.execute(
                    "SELECT DISTINCT value FROM scan_signals WHERE run_id = %s "
                    "AND signal_type = 'service_detected' AND value IS NOT NULL",
                    (run_id,),
                )
                service_names = [r[0] for r in cur.fetchall()]
                if not service_names:
                    raise HTTPException(
                        status_code=404,
                        detail=f"No service_detected signals for run {run_id} — cannot re-render",
                    )

                # risk_signals = the run's non-service signals (same shape as the
                # scout's risk_signals that fed the original generate_all).
                cur.execute(
                    "SELECT signal_type, value, confidence, evidence, source FROM scan_signals "
                    "WHERE run_id = %s AND signal_type <> 'service_detected'",
                    (run_id,),
                )
                risk_signals = [
                    {"signal_type": r[0], "value": r[1], "confidence": r[2],
                     "evidence": r[3] or [], "source": r[4]}
                    for r in cur.fetchall()
                ]

                cur.execute(
                    "SELECT ai_usecase_type FROM project_config WHERE project_name = %s",
                    (project_name,),
                )
                uc = cur.fetchone()
                usecase_types = [uc[0]] if uc and uc[0] else []

        # read-only graph re-query → exact builder shape incl. legal_basis joins
        gc = _get_graph_client()
        graph_result = gc.get_compliance_requirements(
            service_names, run_id=run_id, usecase_types=usecase_types,
        )

        from src.agents.document_architect import DocumentOrchestrator
        generated = DocumentOrchestrator().generate_all(
            graph_result=graph_result,
            reasoning_result={},
            project_name=project_name,
            run_id=run_id,
            risk_signals=risk_signals,
        )
        logger.info("Re-render run %s: %d docs (no re-scan)", run_id, len(generated))
        return {
            "ok": True,
            "run_id": run_id,
            "project_name": project_name,
            "rerendered": len(generated),
            "doc_types": [d.get("doc_type") for d in generated],
        }
    except HTTPException:
        raise
    except Exception as e:
        logger.error("rerender_run failed for %s: %s", run_id, e)
        raise HTTPException(status_code=500, detail="internal error")


