# ── System readiness (first-start honesty; Launch-Gate "Erstkontakt") ────────
# A fresh install shows a finished-looking dashboard while the 9.6-GB model is
# still pulling — a scan would fail cryptically. Three cheap checks feed the
# dashboard status card AND the hard gate on POST /scan.

GRAPH_SEEDED_THRESHOLD = 400  # fresh seed-all writes ~474 nodes; 0 = empty


def _readiness_llm_base() -> str:
    """Ollama base URL (no path): OLLAMA_BASE_URL → OLLAMA_URL minus path → docker default."""
    base = os.getenv("OLLAMA_BASE_URL", "").strip()
    if base:
        return base.rstrip("/")
    full = os.getenv("OLLAMA_URL", "").strip()
    if full:
        return full.split("/api/")[0].rstrip("/")
    return "http://ollama:11434"


def _system_readiness(llm_model: str | None = None) -> dict:
    """Three-light readiness map. Each probe is cheap and hard-timeboxed.

    llm_model overrides the model name to check for — read-only test hook so
    the "model missing" path can be proven without touching the real model.
    """
    checks: dict[str, dict] = {}

    try:
        rows = _get_graph_client().run_query("MATCH (n) RETURN count(n) AS c")
        count = rows[0]["c"] if rows else 0
        if count >= GRAPH_SEEDED_THRESHOLD:
            checks["graph"] = {"ok": True, "detail": f"{count} nodes"}
        elif count > 0:
            checks["graph"] = {"ok": False, "detail": f"only {count} nodes — run: make seed-all"}
        else:
            checks["graph"] = {"ok": False, "detail": "graph is empty — run: make seed-all"}
    except Exception as e:
        logger.warning("readiness graph probe failed: %s", e)
        checks["graph"] = {"ok": False, "detail": "Neo4j unreachable"}

    try:
        if not DB_URL:
            checks["database"] = {"ok": False, "detail": "DATABASE_URL not set"}
        else:
            with psycopg2.connect(DB_URL, connect_timeout=2) as conn:
                with conn.cursor() as cur:
                    cur.execute(
                        "SELECT to_regclass('scan_results') IS NOT NULL"
                        " AND to_regclass('owner_measures') IS NOT NULL"
                    )
                    migrated = bool(cur.fetchone()[0])
            checks["database"] = (
                {"ok": True, "detail": "schema migrated"}
                if migrated
                else {"ok": False, "detail": "schema missing — run: make db-migrate"}
            )
    except Exception as e:
        logger.warning("readiness database probe failed: %s", e)
        checks["database"] = {"ok": False, "detail": "database unreachable"}

    backend = os.getenv("LLM_BACKEND", "local").strip().lower()
    if backend == "local":
        model = (llm_model or os.getenv("OLLAMA_MODEL", "gemma4:e4b")).strip()
        try:
            resp = http_requests.get(f"{_readiness_llm_base()}/api/tags", timeout=3)
            resp.raise_for_status()
            names = [m.get("name", "") for m in resp.json().get("models", [])]
            # tags lists only COMPLETED pulls — a partial download never appears
            present = any(n == model or n.split(":")[0] == model.split(":")[0] for n in names)
            checks["llm"] = (
                {"ok": True, "detail": f"{model} ready"}
                if present
                else {"ok": False, "detail": f"{model} not in local model list — pull likely in progress"}
            )
        except Exception as e:
            logger.warning("readiness llm probe failed: %s", e)
            checks["llm"] = {"ok": False, "detail": "LLM backend unreachable"}
    else:
        key_set = bool(os.getenv("ANTHROPIC_API_KEY", "").strip())
        checks["llm"] = (
            {"ok": True, "detail": "cloud LLM configured"}
            if key_set
            else {"ok": False, "detail": "LLM_BACKEND=api but no API key set"}
        )

    return {"ready": all(c["ok"] for c in checks.values()), "checks": checks}


