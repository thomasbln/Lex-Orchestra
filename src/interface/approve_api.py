def _classify_scan_error(e: Exception) -> str:
    """Short, honest product text for scan_results.error (Launch-Gate row 51).

    The status page shows this string to the user; the raw exception (which may
    carry internal URLs/DSNs) goes to the server log only. Known failure
    classes stay specific — everything else is generic.
    """
    if isinstance(e, http_requests.exceptions.Timeout):
        return f"timeout after {SCAN_WAIT_TIMEOUT}s — the pipeline may still be running"
    text = str(e).lower()
    if "neo4j" in text or "bolt://" in text:
        return "Neo4j unreachable"
    if "ollama" in text or ":11434" in text:
        return "LLM backend unreachable"
    if isinstance(e, http_requests.exceptions.ConnectionError):
        return "scan engine unreachable (LangGraph)"
    return "internal error (see logs)"


